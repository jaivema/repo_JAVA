package ejemplooperadoresaritmeticos;

/**
 *
 * @author udemy
 */
public class EjemploOperadoresAritmeticos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        int a = 5;
        int b = 2;
        
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
        //System.out.println(a/0);  // ERROR!!
    }
    
}
